import express from "express";
import dotenv from "dotenv";
dotenv.config();
import { runSourcingPipeline, runOutreachPipeline, startReplyHandler, handleScreeningCallComplete, sendWarmIntro } from "./pipeline";
const app = express();
app.use(express.json());
const PORT = process.env.PORT || 3001;
app.get('/health', (req, res) => { res.json({ status: 'ok', timestamp: new Date().toISOString() }); });
app.post('/run/source', async (req, res) => { res.json({ status: 'started' }); runSourcingPipeline().catch(console.error); });
app.post('/run/outreach', async (req, res) => { res.json({ status: 'started' }); runOutreachPipeline().catch(console.error); });
app.post('/run/full', async (req, res) => { res.json({ status: 'started' }); (async () => { await runSourcingPipeline(); await runOutreachPipeline(); })().catch(console.error); });
app.post('/webhooks/retell', async (req, res) => { const { event, call } = req.body; res.json({ received: true }); if (event === 'call_ended' && call?.metadata?.candidate_email) handleScreeningCallComplete(call.metadata.candidate_email, call.metadata.nia_candidate_id, call.transcript || '').catch(console.error); });
app.post('/api/warm-intro', async (req, res) => { const { candidateEmail, niaCandidateId } = req.body; res.json({ status: 'started' }); sendWarmIntro(candidateEmail, niaCandidateId).catch(console.error); });
app.listen(PORT, () => { console.log(`OpenRecruiter running on port ${PORT}`); startReplyHandler(); });
export default app;
